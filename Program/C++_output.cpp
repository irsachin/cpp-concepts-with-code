// C++ outputs (Print Text)

/*The cout object, together with the << operator, is used to output values and print text.
Just remember to surround the text with double quotes (""): */

#include <iostream>
using namespace std;

int main() {
  cout << "Hello World! \n"; //print text surround double quotes (""):
  cout << "I am learning C++ \n";
  cout<<10 <<"\n";// print a number
  cout<<4+6<<"\n"; //print the result of an expression
  return 0;
}